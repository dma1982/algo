#include <stdio.h>

int main(int argc, char const *argv[])
{

	int n, i, j;

	char bus[][30] = {
		{"+------------------------+"},
		{"|#.#.#.#.#.#.#.#.#.#.#.|D|)"},
		{"|#.#.#.#.#.#.#.#.#.#.#.|.|"},
		{"|#.......................|"},
		{"|#.#.#.#.#.#.#.#.#.#.#.|.|)"},
		{"+------------------------+"},
	};

	scanf("%d", &n);

	for (i = 0; i < 30; i++)
	{
		for (j = 0; j < 6; j++)
		{
			if (bus[j][i] == '#')
			{
				if (n > 0)
				{
					bus[j][i] = 'O';
					n--;
				}
				else
				{
					goto full;
				}
			}
		}
	}

	full:

	for (i = 0; i < 6; i++)
	{
		printf("%s\n", bus[i]);
	}
	
	return 0;
}


